public class Validar {
    public static boolean esPrimo(int numero){
        assert numero > 1:"El numero"+numero+" ingresado no es valido";
        for (int i = 2; i < numero; i++) {
            if (numero % i == 0) {
                return false;
            }
        }
        return true;
    }
}
